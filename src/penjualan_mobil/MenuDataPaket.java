    public void Tampiltabel(){
        model=new DefaultTableModel();
        model.addColumn("Kode Paket Kredit");
        model.addColumn("Uang Muka");
        model.addColumn("Lama Tenor");
        model.addColumn("Bunga Cicilan");
        tblpaket.setModel(model);
        try {
            this.stat=k.getCon().prepareStatement("select * from paket");
            this.rs=this.stat.executeQuery();
            while(rs.next()){
                Object[] data={
                    rs.getString(1),
                    rs.getInt(2),
                    rs.getInt(3),
                    rs.getInt(4),
                };      
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        txtkode.setText("");
        txtdp.setText("");
        txttenor.setText("");
        txtbunga.setText("");
        
    }
