    class mobil extends MenuDataMobil{
        String kode,merk,type,warna,gambar;
        int harga;
        
        public mobil(){
            this.kode=textkode.getText();
            this.merk=textmerk.getText();
            this.type=texttype.getText();
            this.warna=textwarna.getText();
            this.harga=Integer.parseInt(textharga.getText());
            this.gambar=selectedImagepath;
        }
    }
    public void Tampiltabel(){
        model=new DefaultTableModel();
        model.addColumn("Kode");
        model.addColumn("Merk");
        model.addColumn("Type");
        model.addColumn("Warna");
        model.addColumn("Harga");
        model.addColumn("Gambar Mobil");
        tblmobil.setModel(model);
        
        try {
            this.stat=k.getCon().prepareStatement("Select * From mobil");
            this.rs=this.stat.executeQuery();
            while(rs.next()){
                Object[] data={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getInt(5),
                    rs.getString(6)
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textkode.setText("");
        textharga.setText("");
    }

