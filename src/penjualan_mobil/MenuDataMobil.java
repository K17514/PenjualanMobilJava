    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        try {
            mobil m=new mobil();
            String newFileName="";
            if (!m.gambar.equals("")) {
                File sourceFile= new File(m.gambar);
                File destDir= new File("uploads");
                if (!destDir.exists())destDir.mkdir();
                newFileName="uploads/"+sourceFile.getName();
                Files.copy(sourceFile.toPath(), new File(newFileName).toPath());    
            }
            this.stat=k.getCon().prepareStatement("insert into mobil values(?,?,?,?,?,?)");
            stat.setString(1, m.kode);
            stat.setString(2, m.merk);
            stat.setString(3, m.type);
            stat.setString(4, m.warna);
            stat.setInt(5, m.harga);
            stat.setString(6, newFileName);
            stat.executeUpdate();
            Tampiltabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        
    }//GEN-LAST:event_btntambahActionPerformed

    private void tblmobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblmobilMouseClicked
        textkode.setText(model.getValueAt(tblmobil.getSelectedRow(), 0).toString());
        textmerk.setText(model.getValueAt(tblmobil.getSelectedRow(), 1).toString());
        texttype.setText(model.getValueAt(tblmobil.getSelectedRow(), 2).toString());
        textwarna.setText(model.getValueAt(tblmobil.getSelectedRow(), 3).toString());
        textharga.setText(model.getValueAt(tblmobil.getSelectedRow(), 4).toString());
        selectedImagepath=model.getValueAt(tblmobil.getSelectedRow(), 5).toString();
        if(selectedImagepath!=null && !selectedImagepath.isEmpty()){
            ImageIcon icon=new ImageIcon(new ImageIcon(selectedImagepath).getImage().getScaledInstance(200, 120, java.awt.Image.SCALE_SMOOTH));
            labelgambar.setIcon(icon);
        }
        else {
            labelgambar.setIcon(null);
        }
    }//GEN-LAST:event_tblmobilMouseClicked

    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        try {
            mobil m=new mobil();
            String newFileName="";
            if (!m.gambar.equals("")) {
                File sourceFile= new File(m.gambar);
                File destDir= new File("uploads");
                if (!destDir.exists())destDir.mkdir();
                newFileName="uploads/"+sourceFile.getName();
                Files.copy(sourceFile.toPath(), new File(newFileName).toPath());
            }
            this.stat=k.getCon().prepareStatement("update mobil set merk=?,"+
                    "type=?,warna=?,harga=?,gambar=? where kode_mobil=?");
            stat.setString(1, m.merk);
            stat.setString(2, m.type);
            stat.setString(3, m.warna);
            stat.setInt(4, m.harga);
            stat.setString(5, newFileName);
            stat.setString(6,textkode.getText());
            stat.executeUpdate();
            Tampiltabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnubahActionPerformed

