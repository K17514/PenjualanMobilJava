    class cash extends MenuBeliCash {

        String kode_cash, noktp, nama, kodemobil, merk, tipe, warna, cash_tgl;
        int cash_bayar, harga;

        public cash() {
            this.kode_cash = textkode.getText();
            String combo1 = cbktp.getSelectedItem().toString();
            String[] arr = combo1.split(";");
            this.noktp = arr[0];

            String combo2 = cbkdmobil.getSelectedItem().toString();
            String[] arr1 = combo2.split(":");
            this.kodemobil = arr1[0].split(";")[0];  // "M001"                  // "Silver"
            this.harga = Integer.parseInt(arr1[3]);  // 200000000
            try {
                Date date = datetanggal.getDate();
                DateFormat dateformat = new SimpleDateFormat("YYYY-MM-dd");
                this.cash_tgl = dateformat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }

        }
    }

    public void Tampiltabel() {
        model = new DefaultTableModel();
        model.addColumn("Kode");
        model.addColumn("KTP");
        model.addColumn("Kode Mobil");
        model.addColumn("Tanggal");
        model.addColumn("Total Bayar");
        tblcash.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("Select * from beli_cash");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getInt(5),};
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textkode.setText("");
        txtbayar.setText("");
    }

    public void ComboMobil() {
        try {
            this.stat = k.getCon().prepareStatement("Select * from mobil");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                cbkdmobil.addItem(rs.getString("kode_mobil") + ";"
                        + rs.getString("merk") + ":"
                        + rs.getString("type") + ":"
                        + rs.getString("warna") + ":"
                        + rs.getString("harga"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void ComboPembeli() {
        try {
            this.stat = k.getCon().prepareStatement("Select * from pembeli");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                cbktp.addItem(rs.getString("ktp") + ";"
                        + rs.getString("nama_pembeli")
                );
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

