    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        try {
            cash c = new cash();
            this.stat = k.getCon().prepareStatement("insert into beli_cash values(?,?,?,?,?)");
            stat.setString(1, c.kode_cash);
            stat.setString(2, c.noktp);
            stat.setString(3, c.kodemobil);
            stat.setString(4, c.cash_tgl);
            stat.setInt(5, c.harga);
            stat.executeUpdate();
            Tampiltabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btntambahActionPerformed

    private void tblcashMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcashMouseClicked
        int row = tblcash.getSelectedRow();
        textkode.setText(model.getValueAt(row, 0).toString());

        String ktp = model.getValueAt(row, 1).toString();
        for (int i = 0; i < cbktp.getItemCount(); i++) {
            if (cbktp.getItemAt(i).startsWith(ktp + ":")) {
                cbktp.setSelectedIndex(i);
                break;
            }
        }

        try {
            String tanggal = model.getValueAt(row, 3).toString();
            SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
            Date date = sdf.parse(tanggal);
            datetanggal.setDate(date);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblcashMouseClicked

    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        try {
            cash c = new cash();
            this.stat = k.getCon().prepareStatement("update beli_cash set ktp=?,"
                    + "kode_mobil=?, cash_tgl=?,cash_bayar=? where kode_cash=?");
            stat.setString(1, c.noktp);
            stat.setString(2, c.kodemobil);
            stat.setString(3, c.cash_tgl);
            stat.setInt(4, c.harga);
            stat.setString(5, c.kode_cash);
            stat.executeUpdate();
            Tampiltabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnubahActionPerformed

