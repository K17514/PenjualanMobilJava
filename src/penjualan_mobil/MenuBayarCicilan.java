    private void tblcicilanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcicilanMouseClicked
        int row = tblcicilan.getSelectedRow();
        textkode.setText(model.getValueAt(row, 0).toString());
        String Kredit = model.getValueAt(row, 1).toString();
        for (int i = 0; i < cbkodekredit.getItemCount(); i++) {
            if (cbkodekredit.getItemAt(i).startsWith(Kredit + ":")) {
                cbkodekredit.setSelectedIndex(i);
                break;
            }
        }
        try {
            String tanggal = model.getValueAt(row, 2).toString();
            SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
            Date date = sdf.parse(tanggal);
            datecicilan.setDate(date);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblcicilanMouseClicked

