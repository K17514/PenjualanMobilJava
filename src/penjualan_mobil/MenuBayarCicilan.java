    class cicilan {

        String kodecicilan, kodekredit, tanggal;
        int cicilan, tenor, cicilanke, sisacicil, totalcicil, sisaharga;

        public cicilan() throws Exception {
            this.kodecicilan = textkode.getText();
            String combo = cbkodekredit.getSelectedItem().toString();
            String[] arr = combo.split(":");
            this.kodekredit = arr[0];

            try {
                Date data = datecicilan.getDate();
                DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                this.tanggal = df.format(data);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            PreparedStatement ps = k.getCon().prepareStatement("Select bayar_kredit,tenor,totalcicil from kredit "
                    + "WHERE kode_kredit=?");
            ps.setString(1, kodekredit);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                this.cicilan = rs.getInt("bayar_kredit");
                this.tenor = rs.getInt("tenor");
                this.totalcicil = rs.getInt("totalcicil");
            } else {
                throw new Exception("Data Tidak Bisa ditemukan!");
            }

            //hitung cicilan ke-
            ps = k.getCon().prepareStatement("SELECT COUNT(*) AS total FROM bayar_cicilan "
                    + "WHERE kode_kredit=? ");
            ps.setString(1, kodekredit);
            rs = ps.executeQuery();
            if (rs.next()) {
                this.cicilanke = rs.getInt("total") + 1;
            } else {
                this.cicilanke = 1;
            }
            this.sisacicil = tenor - cicilanke;
            this.sisaharga = totalcicil - (cicilanke * cicilan);
        }
    }

    public void Tampiltabel() {
        model = new DefaultTableModel();
        model.addColumn("Kode Cicilan");
        model.addColumn("Kode Kredit");
        model.addColumn("Tanggal");
        model.addColumn("Cicilan Ke-");
        model.addColumn("Besar Cicilan");
        model.addColumn("Sisa Cicilan");
        model.addColumn("Sisa Harga");
        tblcicilan.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("Select * from bayar_cicilan");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getInt(4),
                    rs.getInt(5),
                    rs.getInt(6),
                    rs.getInt(7),};
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textkode.setText("");
    }

    public void ComboKredit() {
        try {
            this.stat = k.getCon().prepareStatement("Select * from kredit");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                cbkodekredit.addItem(rs.getString("kode_kredit") + ":"
                        + rs.getString("ktp") + ":"
                        + rs.getString("kode_paket") + ":"
                        + rs.getString("kode_mobil") + ":"
                        + rs.getString("tanggal_kredit") + ":"
                        + rs.getString("bayar_kredit") + ":"
                        + rs.getString("tenor") + ":"
                        + rs.getString("totalcicil"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

